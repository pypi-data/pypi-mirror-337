def font_text(text, style="bold"):
    """Convert text to the specified font style."""
    styled_text = []
    for char in text:
        # Get the font mapping for the specified style
        font_mapping = FONT_STYLES.get(style, {})
        # Get the styled character or default to the original character
        styled_char = font_mapping.get(char, char)
        # Append the styled character to the result list
        styled_text.append(styled_char)
    # Join the list into a single string and return it
    return "".join(styled_text)

